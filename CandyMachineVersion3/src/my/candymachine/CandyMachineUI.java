    public void dispenseProduct(JButton button, Dispenser item, String itemName) {
        int pay = Integer
                .parseInt(JOptionPane.showInputDialog(null, "To buy a " + itemName + ", please insert 50 cents."));
        if (pay >= item.getProductCost()) {
            if (item.getCount() > 0) {
                cashier.acceptAmount(pay);
                JOptionPane.showMessageDialog(null, "Please pickup your " + itemName);
                item.makeSale();
            }
        } else {
            JOptionPane.showMessageDialog(null, "You did not enter the required payment.");
        }

    }

